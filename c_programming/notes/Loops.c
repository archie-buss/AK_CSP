#include <stdio.h>
#include <string.h>


int main(void) {
    int nums[] = {1,2,3,4,5,6,7};
    char chandies[5][20] = {"KitKat", "skittles", "snickers", "reeses", "twix"};

    for(int x = 0; x < 7; x++) {
        printf("%d\n", nums[x]);
    }

    for (int i = 0; i < 5; i++) {
        printf("%s\n", chandies[i]);
    }
    return 0;
}


